function populateGallery(monthId, images, folderPath) {
    const monthDiv = document.getElementById(monthId);
    const galleryDiv = monthDiv.querySelector('.lightgallery');

    images.forEach(filename => {
      const a = document.createElement('a');
      a.href = `${folderPath}/${filename}`;
      a.setAttribute('data-lg-size', '1600-1067');

      const img = document.createElement('img');
      img.src = `${folderPath}/${filename}`;
      img.style.width = '150px'; // or any size you like

      a.appendChild(img);
      galleryDiv.appendChild(a);
    });

    lightGallery(galleryDiv, {
      plugins: [lgZoom, lgThumbnail],
      licenseKey: '0000-0000-000-0000',
      speed: 500
    });
  }

